use v5.36;
use Data::Dumper; $Data::Dumper::Sortkeys = 1;


my @range;
my $includes = 0;
while (<>)
{
    chomp;

    my ($l1, $u1, $l2, $u2) = split /[-,]/;

    if ( ( $l1 >= $l2 && $u1 <= $u2 )  ||  ( $l2 >= $l1 && $u2 <= $u1 ) )
    {
        say "Y [$l1 - $u1] IN [$l2 - $u2]";
        $includes++;
    }
    else
    {
        say "N [$l1 - $u1] IN [$l2 - $u2]";
    }
}

say $includes;
